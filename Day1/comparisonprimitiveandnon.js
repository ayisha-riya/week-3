    // Primitive vs Non-Primitive Data Types
//----------------------------------------------- 
// Feature      	   // Primitive	                      //Non-Primitive
//------------------------------------------------------------------------------------------
// Meaning      	    Holds a single simple value	       Holds multiple or complex values

// Examples	            String, Number, Boolean, Null,     Object, Array, Function
//                       Undefined, Symbol, BigInt	

// Storage	            Stored by value (actual data)	    Stored by reference (memory address)

// Mutable / Changeable	Immutable – value can’t be 
//                      changed directly	                 Mutable – values inside can be changed

// Comparison	        Compared by value	                 Compared by reference

// Copy Behavior	    Copy makes a new value	             Copy shares the same reference

// Type of Data	          Simple	                         Complex